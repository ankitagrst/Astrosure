export async function geocodePlace(place: string): Promise<{
  lat: number
  lng: number
  timezone: number
  formattedPlace: string
}> {
  // Normalize the place name (lowercase, trim)
  const normalizedPlace = place.toLowerCase().trim()
  
  // Try to find exact match
  if (CITY_COORDINATES[normalizedPlace]) {
    const coords = CITY_COORDINATES[normalizedPlace]
    return {
      lat: coords.lat,
      lng: coords.lng,
      timezone: coords.timezone,
      formattedPlace: place.charAt(0).toUpperCase() + place.slice(1),
    }
  }
  
  // Try to find partial match (contains the city name)
  for (const [city, coords] of Object.entries(CITY_COORDINATES)) {
    if (normalizedPlace.includes(city) || city.includes(normalizedPlace)) {
      return {
        lat: coords.lat,
        lng: coords.lng,
        timezone: coords.timezone,
        formattedPlace: place.charAt(0).toUpperCase() + place.slice(1),
      }
    }
  }

  const { UserError } = await import('../errors')
  throw new UserError(`Unable to geocode place: ${place}`, `Could not find the location "${place}". Try specifying a city, state or country (for example: "Mumbai, India").`, 422)
}

